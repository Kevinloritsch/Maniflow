analysis_prompt = '''
    You are a strict quality reviewer for algorithm visualization videos generated by Manim. Your job is to identify every visual error present in this video with enough specificity that a developer can fix the exact lines of code responsible — your goal is to determine whether a student watching this video could learn from it effectively.

    Watch the entire video carefully before responding.

    Review the video for each of the following categories and report findings for every single one:

    ---

    LAYOUT & BOUNDS
    - Are any objects (nodes, bars, labels, arrows, tables) partially or fully outside the visible frame?
    - Do any objects overlap each other in a way that makes either one unreadable?
    - Is the title or any text cut off at the edges?
    - Is the overall layout balanced, or is everything crammed to one side?

    TEXT & LABELS
    - Are all node labels, edge weights, bar values, and index markers clearly visible?
    - Is any text too small to read?
    - Does any text overlap with a shape or another piece of text?
    - Are all labels correctly positioned relative to their corresponding object (e.g. edge weight labels sitting on or near their edge)?

    COLOR & HIGHLIGHTING
    - Is there a consistent color system (e.g. unvisited=blue, visiting=yellow, visited=green, sorted=green, pivot=red)?
    - Does any element fail to change color when it should based on the algorithm step?
    - Are any two states using the same color making them indistinguishable?
    - Do colors ever fail to reset after a step completes?

    ANIMATION & TIMING
    - Are there any steps where nothing happens on screen but time passes (frozen/dead frames)?
    - Do any animations happen so fast they are impossible to follow?
    - Do any two animations overlap when they should be sequential?
    - Does any object jump or teleport instead of animating smoothly?
    - Are there any flickering or rendering glitches?

    ALGORITHM CORRECTNESS (visual only)
    - Does the visualization appear to follow a logical step-by-step progression?
    - For sorting: do bars end up in increasing order left to right?
    - For graph algorithms: does the traversal appear to visit nodes in a connected, logical order?
    - For tree algorithms: does the structure maintain valid tree shape throughout?
    - Are there any steps that appear to be skipped entirely?
    - Does any data structure (table, list, tree) appear to update at the wrong time?

    POINTERS & INDICATORS
    - Are all pointer arrows or index markers visible and correctly positioned below or beside their target?
    - Do pointers move smoothly and stay in sync with the current algorithm step?
    - Are pointer labels (i, j, pivot, current, etc.) visible and readable throughout?

    COMPLETION
    - Does the video reach a clear finished state (e.g. fully sorted array, all nodes visited)?
    - Is there a success message or final state indicator visible at the end?
    - Does the video end abruptly before the algorithm completes?

    ---

    Respond in this exact JSON format:

    {
      "passed": true or false,
      "overall_summary": "One sentence describing the overall quality",
      "errors": [
        {
          "category": "category name from above",
          "severity": "critical | major | minor",
          "description": "Exactly what is wrong and at what point in the video",
          "timestamp": "approximate time in video e.g. 0:03-0:05",
          "likely_code_cause": "What kind of code issue probably caused this",
          "suggested_fix": "Specific actionable fix for the developer"
        }
      ],
      "passed_checks": ["list of categories that had no issues"],
      "iteration_recommendation": "fix_and_retry | minor_cleanup | approve"
    }

    Set "passed" to true only if there are zero critical errors and fewer than two major errors.
    Set "iteration_recommendation" to:
    - "fix_and_retry" if any critical errors exist
    - "minor_cleanup" if only minor errors exist
    - "approve" if passed is true and the video is ready to save to the dataset
'''

class AnalyzeRequest(BaseModel):
    video_path: str

@app.post("/gemini_analysis")
def analyze_video(body: AnalyzeRequest):
    
    print(f"Received video_path: {body.video_path}")
    client = genai.Client(api_key=os.getenv("GEMINI_API_KEY"))
    
    video_file = client.files.upload(
        file=body.video_path,
        config={"mime_type": "video/mp4"}
    )
    print(f"Uploaded: {video_file.name}, state: {video_file.state}")

    while video_file.state.name == "PROCESSING":
        print("Waiting for file to process...")
        time.sleep(3)
        video_file = client.files.get(name=video_file.name)
    
    if video_file.state.name == "FAILED":
        raise RuntimeError("Gemini file processing failed")

    print("File ready, analyzing...")

    response = client.models.generate_content(
        model="gemini-3-flash-preview",
        contents=[
            types.Part(file_data=types.FileData(file_uri=video_file.uri)),
            types.Part(text=analysis_prompt)
        ]
    )
    print(response.text)
    
    client.files.delete(name=video_file.name)

    raw = response.text.strip()
    if raw.startswith("```"):
        raw = raw.split("```")[1]
        if raw.startswith("json"):
            raw = raw[4:]
    
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        return {"raw_response": response.text}
    

